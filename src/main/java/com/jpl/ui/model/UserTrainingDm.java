package com.jpl.ui.model;

public class UserTrainingDm {

	private String searchTraining;
	private String trainingName;
	private String trainingWebsite;
	private String trainingAddress;
	private String requestTraining;

	public String getSearchTraining() {
		return searchTraining;
	}

	public void setSearchTraining(String searchTraining) {
		this.searchTraining = searchTraining;
	}

	public String getTrainingName() {
		return trainingName;
	}

	public void setTrainingName(String trainingName) {
		this.trainingName = trainingName;
	}

	public String getTrainingWebsite() {
		return trainingWebsite;
	}

	public void setTrainingWebsite(String trainingWebsite) {
		this.trainingWebsite = trainingWebsite;
	}

	public String getTrainingAddress() {
		return trainingAddress;
	}

	public void setTrainingAddress(String trainingAddress) {
		this.trainingAddress = trainingAddress;
	}

	public String getRequestTraining() {
		return requestTraining;
	}

	public void setRequestTraining(String requestTraining) {
		this.requestTraining = requestTraining;
	}

}
